const size_t diameter_len = sizeof(diameter_msg);
std::vector<uint8_t> buildCEA(const DiameterMsg& cer);

void printRawHex(const uint8_t* data, size_t len)
{
    cout << "========== RAW DIAMETER PACKET ==========" << endl;

    for(size_t i = 0; i < len; i++)
    {
        if(i % 16 == 0)
            cout << endl << setw(4) << setfill('0') << hex << i << "  ";

        cout << setw(2) << setfill('0') << hex << int(data[i]) << " ";
    }

    cout << dec << endl << endl;
}

int main()
{
    DiameterMsg cerMsg;

    cout << "\n========== INPUT CER ==========" << endl;
    printRawHex(diameter_msg, diameter_len);

    // ===== STEP 1: DECODE CER =====
    if(cerMsg.decoder(diameter_msg, diameter_len) != 0)
    {
        cout << "[ERROR] Decode CER failed!" << endl;
        return -1;
    }

    cout << "\n[INFO] Decode CER success!" << endl;
    cerMsg.printAll();

    // ===== STEP 2: VALIDATE CER =====
    cout << "\n========== CER VALIDATION ==========" << endl;

    if(!processCER(cerMsg))
    {
        cout << "[ERROR] CER invalid -> Abort connection" << endl;
        return -1;
    }

    cout << "[INFO] CER valid -> Build CEA" << endl;

    // ===== STEP 3: BUILD CEA =====
    vector<uint8_t> ceaBuffer = buildCEA(cerMsg);

    // ===== STEP 4: PRINT RAW CEA =====
    cout << "\n========== OUTPUT CEA (RAW) ==========" << endl;
    printRawHex(ceaBuffer.data(), ceaBuffer.size());

    // ===== STEP 5: DECODE AGAIN (VERIFY) =====
    DiameterMsg ceaMsg;

    if(ceaMsg.decoder(ceaBuffer.data(), ceaBuffer.size()) != 0)
    {
        cout << "[ERROR] Decode CEA failed!" << endl;
        return -1;
    }

    cout << "\n[INFO] Decode CEA success!" << endl;
    ceaMsg.printAll();

    // ===== STEP 6: HANDSHAKE DONE =====
    cout << "\n=====================================" << endl;
    cout << "[INFO] Diameter Handshake SUCCESS" << endl;
    cout << "[FLOW] CER -> CEA completed" << endl;
    cout << "=====================================" << endl;

    return 0;
}
